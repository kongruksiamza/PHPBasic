<?php
/*
5 ปึ => 10%
10 ปี => 20 %
*/
$year = 15;

switch($year){
    case 5 :
        echo "ระยะเวลากู้ = ".$year." ปี คิดดอกเบี้ย 10%";
        break;
    case 10 :
        echo "ระยะเวลากู้ = ".$year." ปี คิดดอกเบี้ย 20%";
        break;
    default:
        echo "ไม่เข้าเงื่อนไขตามที่กำหนด";
}

?>